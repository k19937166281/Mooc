package mooc.MOOC0802;

public class MathTest {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-5));//取绝对值
		System.out.println(Math.max(-5,-8));//取二者之间大的
		System.out.println(Math.pow(-5,2));//幂计算  此处为   -5的2次方
		System.out.println(Math.round(3.5));//四舍五入
		System.out.println(Math.ceil(3.5));//取得整数位+1
		System.out.println(Math.floor(3.5));//取得整数位
	}

}
